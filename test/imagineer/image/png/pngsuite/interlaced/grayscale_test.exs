  defp assert_pixels_match(parsed_pixels, actual_pixels) do
    Enum.zip(parsed_pixels, actual_pixels)
    |> Enum.with_index
    |> Enum.each(fn
      ({{parsed, actual}, index}) ->
        assert(actual == parsed, """
          row #{index + 1} is fucked
          \texpected: #{inspect actual}
          \tgot: #{inspect parsed}
        """)
    end)
    assert parsed_pixels == actual_pixels, """
    There are no rows that don't match, but you're still fucked.
    Got: #{inspect(parsed_pixels)}
    Expected: #{inspect(actual_pixels)}
    """
  end

  defp extract_single_channel_pixels(image) do
    Enum.map(image.pixels, fn (row) ->
      Enum.map(row, &elem(&1, 0))
    end)
  end
